package com.practice.DataTypes;

/**
 * 
 * data type are specify the different sizes and values that can be are stored
 * into the variables
 * 
 * there are two types of data types 1) primitive data types --->char,
 * byte,short, int,long,double,float, boolean
 * 
 * 2)Non primitive data types --->classes, interfaces, arrays
 */
public class DataTypes {

	public static void main(String[] args) {

		int a = 1023234234;
		char ch = 'd';
		byte b = 127;
		short s = 10000;
		long l = 12345689006L;
		double d = 23.3;
		float f = 23.45f;
		boolean tf = true;

		System.out.println(a);
		System.out.println(ch);
		System.out.println(b);
		System.out.println(s);
		System.out.println(l);
		System.out.println(d);
		System.out.println(f);
		System.out.println(tf);
	}

}
